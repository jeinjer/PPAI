class Validacion:
    def __init__(self, audioMensajeValidacion, nombre):
        self.audioMensajeValidacion = audioMensajeValidacion
        self.nombre = nombre

    def setAudioMensajeValidacion(self, audioMensajeValidacion):
        self.audioMensajeValidacion = audioMensajeValidacion

    def getAudioMensajeValidacion(self):
        return self.audioMensajeValidacion
    
    def setNombreValidacion(self, nombre):
        self.nombre = nombre

    def getNombreValidacion(self):
        return self.nombre


